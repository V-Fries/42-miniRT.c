#include "ft_linked_list.h"
#include <stdlib.h>

void	ft_lstclear(t_list **lst, void (*del)(void *))
{
	t_list	*next;

	if (lst == NULL)
		return ;
	while (*lst)
	{
		next = (*lst)->next;
		if (del != NULL)
			del((*lst)->content);
		free(*lst);
		*lst = next;
	}
}
